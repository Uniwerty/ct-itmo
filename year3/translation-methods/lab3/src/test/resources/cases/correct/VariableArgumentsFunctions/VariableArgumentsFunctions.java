public class Program {
    public int alpha(int arg0, int arg1) {
        return arg0 * arg1 - arg0 * arg0;
    }

    public boolean beta(int arg0, boolean arg1) {
        return arg1;
    }

    public boolean gamma(boolean arg0, boolean arg1) {
        return !(arg0) || arg0 && arg1 && arg1;
    }
}